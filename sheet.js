// vim cheat cheat
// j-k: up/ down
// h-l: forward/backword
// w-b: words forward / backword
// i: insert mode
// capslock (ctr) - esc :  normal mode
// c: change command
// C: change rest of line
// d: delete command
// r: rename command
// D: delete rest of line
// f: find command line
// ;: next instance of find command
// ,: previous instance of find command
// {}: next/previous block
// *: next instance of word
// 0: begin line
// .: redo last command
// (ctrl r): redo
// u: undo
// I: start of line
// A: end of line
// v: visual select
// v: visual select line
// (ctr) v: visual block
// zz: center page to cursor
// %: to next curly
// q + key: record macro
// @ + key: play macro
// /: search word in doc
// ?: search word in doc backwards
// n: skip to next instance of word
// N: skip to previous instance of word
// gg: jump to line
// ~: caps lock
// gi: jump back to last insert place
// gb: add cursor to next instance
// gh: information

